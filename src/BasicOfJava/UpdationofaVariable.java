package BasicOfJava;

public class UpdationofaVariable {
    public static void main(String[] args) {
        int x;//ek dabba ban gaya
        x=500; //dabbe me 500 ko dal diya
        System.out.println(x);//output screen  visible, next line provided
        x =x+5; //x dabbe me x+5 dal do(500+5 ko dal do)
        System.out.println(x);
        x = x-10;
        System.out.println(x);
        x= 5;
        System.out.println(x);
        x = 7;
        System.out.println(x);
        x = x+6;
        System.out.println(x);
        x = x-20;
        System.out.println(x);
    }
}
