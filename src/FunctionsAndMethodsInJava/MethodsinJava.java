package FunctionsAndMethodsInJava;

public class MethodsinJava {
    /*
    Object in Java
    Object has two parts:-
    *Has-part (State/Properties) (Fields/Variables)
    *Does-Part (Behaviour/Activity)(Methods/Functions)
    1. A method , like a function , is a block of code or collection of statements grouped together to perform a certain task or operation
    The difference is that a method is associated with an object, while a function is not.
    2. In Java, methods are used to implement the behaviour of the object.
    3. A method must always be declared within a class.
    4. A method can directly access all the instance variables of the enclosing class.

    DEFINITION OF METHODS IN JAVA:-
 modifiers return_type method_name(parameters)exception_list
           {
             //body of the method
}
Method declaration has six components, in order:
1. Modifiers - Decides the visibility and accessibility of the method.
2. The return type - the data type of the value returned by the method, or void if the method does not return a value.
3. The method name - identifier, name of the behaviour
4. The parameter list in parenthesis- a comma-separated list of input parameters, preceded by their data type. If there are no parameters, we must use empty parentheses.
5. An exception list -  list of exceptions ducked.
6. The method body, enclosed between braces- the method's code, activity/behaviour exhibited by the object.
*/
}
